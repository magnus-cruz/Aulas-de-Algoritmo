x= float(input("Digite um número: "))
y= float(input("Digite outro número: "))
soma = x + y
print(f"A soma de {x} e {y} é igual a {soma}")
subtracao = x - y
print(f"A subtração de {x} e {y} é igual a {subtracao}")
multiplicacao = x * y 
print(f"A multiplicação de {x} e {y} é igual a {multiplicacao}")
divisao = x / y
print(f"A divisão de {x} e {y} é igual a {divisao}")
resto = x % y   
print(f"O resto da divisão de {x} por {y} é igual a {resto}")
potencia = x ** y
print(f"{x} elevado a {y} é igual a {potencia}")
